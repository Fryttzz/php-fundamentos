<?php
//Soma
$a = 10;
$a += $a;

echo $a . "\n";

//Subtração
$b = 10;
$b -= $b;
echo $b . "\n";


//Multiplicação
$c = 10;
$c *= $c;
echo $c . "\n";

//Divisão
$d = 10;
$d /= 2;
echo $d . "\n";

//Módulo
$e = 10;
$e %= 2;
echo $e . "\n";

//Incremento e Decremento
$var = 10;
echo "Var for incrementada " . ++$var . PHP_EOL;
echo "Var for decrementada " . --$var;